;; ## Macroexpand-Deep - the first Clojure AI conference
;; 
;; The Macroexpand-Deep conference focuses the development of research around AI systems in Clojure. It is one of the [Macroexpand 2025](./) pair of conferences.
;; 
;; Here, by the obviously-vague term 'AI systems', we typically mean systems that involve the composition of deep networks with other components. However, we are open to alternative interpretations, of course.
;; 
;; ### Preferred topics
;; (tentative)
;; 
;; (Please see the [Speakers](./speakers.html) page for guidelines.)
;; 
;; * Building systems with LLMs and embeddings
;; * Testing algorithms in specific scenarios
;; * Creating and training network architectures
;; * Understanding trained networks (e.g., mechanistic interpretability)
;; * New tools and improvements
